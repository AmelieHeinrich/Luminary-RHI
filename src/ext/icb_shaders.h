static const char* dispatch_icb_conversion_shader =
"#include <metal_stdlib>\n"
"using namespace metal;\n"
"\n"
"struct DispatchCommand {\n"
"    uint group_count_x;\n"
"    uint group_count_y;\n"
"    uint group_count_z;\n"
"};\n"
"\n"
"struct Arguments {\n"
"    command_buffer cmd_buffer;\n"
"    \n"
"    uint threads_per_group_x;\n"
"    uint threads_per_group_y;\n"
"    uint threads_per_group_z;\n"
"};\n"
"\n"
"kernel void encode_draws(\n"
"    device const DispatchCommand* dispatch_command [[buffer(0)]],\n"
"    device Arguments* args [[buffer(1)]])\n"
"{\n"
"    const DispatchCommand dispatch = dispatch_command[0];\n"
"\n"
"    compute_command cc(args->cmd_buffer, 0);\n"
"    cc.concurrent_dispatch_threadgroups(\n"
"        uint3(dispatch.group_count_x, dispatch.group_count_y, dispatch.group_count_z),\n"
"        uint3(args->threads_per_group_x, args->threads_per_group_y, args->threads_per_group_z)\n"
"    );\n"
"}\n";

