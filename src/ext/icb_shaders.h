static const char* draw_icb_conversion_shader =
"#include <metal_stdlib>\n"
"using namespace metal;\n"
"\n"
"struct DrawCommand {\n"
"    uint draw_id;\n"
"    uint vertex_count;\n"
"    uint instance_count;\n"
"    uint first_vertex;\n"
"    uint first_instance;\n"
"};\n"
"\n"
"struct PerDrawConstant {\n"
"    uint data[32];\n"
"    uint draw_id;\n"
"    uint _pad[31];\n"
"};\n"
"\n"
"struct Arguments {\n"
"    command_buffer cmd_buffer;\n"
"};\n"
"\n"
"kernel void encode_draws(\n"
"    device const DrawCommand* draw_commands       [[buffer(0)]],\n"
"    device const uint*        draw_count          [[buffer(1)]],\n"
"    device const uint*        primitive_type_buf  [[buffer(2)]],\n"
"    device Arguments*         args                [[buffer(3)]],\n"
"    device PerDrawConstant*   per_draw_constants  [[buffer(4)]],\n"
"    device const uint*        resource_heap       [[buffer(5)]],\n"
"    device const uint*        sampler_heap        [[buffer(6)]],\n"
"    device atomic_uint*       draw_id_counter     [[buffer(7)]],\n"
"    uint gid [[thread_position_in_grid]])\n"
"{\n"
"    if (gid >= *draw_count)\n"
"        return;\n"
"\n"
"    const DrawCommand draw = draw_commands[gid];\n"
"    device PerDrawConstant* dst = per_draw_constants + gid;\n"
"\n"
"    // Copy base constants from slot 0\n"
"    if (gid > 0) {\n"
"        device uint8_t* dPtr = reinterpret_cast<device uint8_t*>(dst);\n"
"        device const uint8_t* sPtr = reinterpret_cast<device const uint8_t*>(per_draw_constants);\n"
"        for (uint i = 0; i < 128; ++i)\n"
"            dPtr[i] = sPtr[i];\n"
"    }\n"
"\n"
"    dst->draw_id = atomic_fetch_add_explicit(draw_id_counter, 1, memory_order_relaxed);\n"
"\n"
"    uint primitive = primitive_type_buf[0];\n"
"    primitive_type pt;\n"
"    switch (primitive) {\n"
"        case 0: pt = primitive_type::point;    break;\n"
"        case 1: pt = primitive_type::line;     break;\n"
"        case 2: pt = primitive_type::triangle; break;\n"
"        default: return;\n"
"    }\n"
"\n"
"    render_command rc(args->cmd_buffer, gid);\n"
"    rc.set_vertex_buffer(resource_heap, 0);\n"
"    rc.set_fragment_buffer(resource_heap, 0);\n"
"    rc.set_vertex_buffer(sampler_heap, 1);\n"
"    rc.set_fragment_buffer(sampler_heap, 1);\n"
"    rc.set_vertex_buffer(dst, 2);\n"
"    rc.set_fragment_buffer(dst, 2);\n"
"    rc.draw_primitives(pt, draw.first_vertex, draw.vertex_count, draw.instance_count, draw.first_instance);\n"
"}\n";

